from typing import Any
import timeit


def flatten(lst, acc=None):
    if acc is None:
        acc = []
    if not lst:
        return acc
    head, *tail = lst
    if isinstance(head, list):
        return flatten(head + tail, acc)
    return flatten(tail, acc + [head])


